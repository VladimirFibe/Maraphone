 */

var aYear =  Int.random(in: 0..<2099)
func isLeap(year: Int) {
    if year % 400 == 0 {
        print("\(year) - четное")
    } else if year % 4 == 0 && !(year % 100 == 0) {
        print("\(year) - четное")
    } else {
        print("\(year) - нечетное")
    }
}

